const numbers = [1, 2, 3, 5]
console.log(numbers)

//adding elements to array (at beggining)
numbers.unshift(-1, 0)
console.log(numbers)
//deleting first element in array (with index [0])
numbers.shift()
console.log(numbers)

//new array
const colors = ['red', 'green', 'blue']
console.log(colors)

//adding elements to the end of array
colors.push('gold', 123)
console.log(colors)

//deleting the last element on array
colors.pop()
console.log(colors)

